    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int idCancion = Integer.parseInt(txtIdCancion.getText());
     
        String generoMusica = cbxGeneros.getSelectedItem().toString();
        String album = cbxAlbums.getSelectedItem().toString();
        String nombreCancion = txtNombreCancion.getText();
        Date creadoEl = new Date();
        
        String artista = cbxArtistas.getSelectedItem().toString();
        char idTemporal = artista.charAt(0);
        int idArtista = Character.getNumericValue(idTemporal);

        Album albumEncontrado = Album.albums.stream().filter(a -> a.getNombre().equals(album))
                .findFirst()
                .orElse(null);
        Genero generoENcontrado = Genero.generos.stream().filter(g -> g.getNombre().equals(generoMusica))
                .findFirst()
                .orElse(null);

        Cancion cancionEncontrado = Cancion.canciones.stream().filter(c -> c.getId() == idCancion)
                .findFirst()
                .orElse(null);

        if (cancionEncontrado == null) {
            Cancion nuevaCancion = new Cancion();
            nuevaCancion.setNombre(nombreCancion);
            nuevaCancion.setAlbumId(albumEncontrado.getId());
            nuevaCancion.setCreadoEl(creadoEl);
            nuevaCancion.setArtistaId(idArtista);
            nuevaCancion.setGeneroId(generoENcontrado.getId());
            cancionDAO.guardarCancion(nuevaCancion);
            Cancion.canciones.clear();
            cancionDAO.obtenerCancion();
            Servicio.limpiarTabla(tblCanciones);
            mostrarDatosEnTabla(tblCanciones);
            JOptionPane.showMessageDialog(null, "Registro Guardado!");
            limpiarDatos();
        } else {
            limpiarDatos();
            JOptionPane.showMessageDialog(null, "Cancion Ya existe con el mismo ID!");
        }


    }//GEN-LAST:event_jButton1MouseClicked

