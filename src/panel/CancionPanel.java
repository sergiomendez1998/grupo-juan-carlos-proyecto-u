    private void cbxArtistasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxArtistasItemStateChanged
        // TODO add your handling code here:
        try {
            String artista = cbxArtistas.getSelectedItem().toString();
            char idTemporal = artista.charAt(0);
            int idArtista = Character.getNumericValue(idTemporal);
            cbxAlbums.removeAllItems();
            Album.albums.stream().forEach(album -> {
                if (album.getArtistaId() == idArtista) {
                    cbxAlbums.addItem(album.getNombre());

                }
            });

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_cbxArtistasItemStateChanged

