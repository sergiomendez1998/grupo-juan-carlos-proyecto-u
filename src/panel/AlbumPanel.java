    public void mostrarDatosEnTabla(JTable tabla) {
        Object[] datos = new Object[10];
        Album.albums.sort(Comparator.comparing(Album::getId));

        model = (DefaultTableModel) tabla.getModel();
        Album.albums.stream().forEach(a -> {
            Artista artista = Servicio.buscarArtistaPorId(a.getArtistaId());

            if (artista != null) {
                datos[0] = a.getId();
                datos[1] = a.getNombre();
                datos[2] = a.getAnio();
                datos[3] = artista.getNombre();
            } else {
                datos[0] = a.getId();
                datos[1] = a.getNombre();
                datos[2] = a.getAnio();
            }

            model.addRow(datos);
        });
        tabla.setModel(model);
    }

