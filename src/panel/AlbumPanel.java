    private void tblAlbumsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlbumsMouseClicked
        // TODO add your handling code here:

        int fila = tblAlbums.getSelectedRow();
        if (fila == -1) {
            System.out.println("seleccione una fila");

        } else {

            txtIdAlbum.setText((String) tblAlbums.getValueAt(fila, 0).toString());
            txtNombreAlbum.setText((String) tblAlbums.getValueAt(fila, 1).toString());
            txtAnioAlbum.setText((String) tblAlbums.getValueAt(fila, 2).toString());
            int id = Integer.parseInt(tblAlbums.getValueAt(fila, 0).toString());
            Album albumEncontrado = Album.albums.stream().filter(album -> album.getId() == id)
                    .findFirst()
                    .orElse(null);
            Artista artista = Servicio.buscarArtistaPorId(albumEncontrado.getArtistaId());
            if (artista == null) {
                txtIDAlbumArtista.setText("0");
                txtNombreAlbumArtista.setText("");
            } else {
                String idArtista = String.valueOf(artista.getId());
                String nombrArtista = String.valueOf(artista.getNombre());
                txtIDAlbumArtista.setText(idArtista);
                txtNombreAlbumArtista.setText(nombrArtista);
            }
        }
    }//GEN-LAST:event_tblAlbumsMouseClicked

